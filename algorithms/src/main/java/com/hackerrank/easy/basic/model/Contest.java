package com.hackerrank.easy.basic.model;

public class Contest {

	public int contst;
	public int imp;

	public Contest(int contst, int imp) {
		this.contst = contst;
		this.imp = imp;
	}

	public int getContst() {
		return contst;
	}

	public void setContst(int contst) {
		this.contst = contst;
	}

	public int getImp() {
		return imp;
	}

	public void setImp(int imp) {
		this.imp = imp;
	}

}
